public class Constantes {
    public static void main(String[] args) {

        // Declaración de una constante de tipo float con el valor de pi
        // La palabra clave 'final' indica que el valor de 'pi' no puede cambiar después de ser inicializado
        final float pi = 3.14159265359f;

        // Imprimir el valor de la constante 'pi' en la consola
        System.out.println("El número de pi es: " + pi);
    }
}
